#include "libft.h"

void	ft_tabrev(char **s, int len)
{
	char	*buf;
	int		i;

	i = -1;
	while (++i < len / 2)
	{
		buf = s[i];
		s[i] = s[len - i - 1];
		s[len - i - 1] = buf;
	}
}
