#include "libft.h"

char	*ft_strrev(char *str)
{
	char	tmp;
	size_t	len;
	size_t	i;

	i = 0;
	len = ft_strlen(str);
	while (i < len / 2)
	{
		tmp = str[i];
		str[i] = str[len - i - 1];
		str[len - i - 1] = tmp;
		i++;
	}
	return (str);
}
